package com.example.oddeven;

public class OddEven {

	public static void main(String[] args) {
		//Even Odd Checking
				int a=10;
				int b = 30;
				for(int i=a;i<b;i++) {
					if(i%2==0) {
						System.out.println("Even number:"+i);
					}
					else {
						System.out.println("Odd number:"+i);
					}
				}

	}

}
